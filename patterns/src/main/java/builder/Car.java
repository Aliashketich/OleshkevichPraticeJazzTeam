package builder;

class Car {
    private String mark;
    private String color;
    private double cost;

    String getMark() {
        return mark;
    }

    void setMark(String mark) {
        this.mark = mark;
    }

    double getCost() {
        return cost;
    }

    void setCost(double cost) {
        this.cost = cost;
    }

    String getColor() {
        return color;
    }

    void setColor(String color) {
        this.color = color;
    }
}
