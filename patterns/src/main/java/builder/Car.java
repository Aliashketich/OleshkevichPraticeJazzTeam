package builder;

public class Car {
    private String mark;
    private int speed;
    private String color;

    public void setMark(String mark) {
        this.mark = mark;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMark() {
        return mark;
    }

    public int getSpeed() {
        return speed;
    }

    public String getColor() {
        return color;
    }
}
