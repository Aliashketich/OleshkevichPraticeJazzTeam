package builder;

class Car {
    private String mark;
    private int speed;
    private String color;

    String getMark() {
        return mark;
    }

    void setMark(String mark) {
        this.mark = mark;
    }

    int getSpeed() {
        return speed;
    }

    void setSpeed(int speed) {
        this.speed = speed;
    }

    String getColor() {
        return color;
    }

    void setColor(String color) {
        this.color = color;
    }
}
